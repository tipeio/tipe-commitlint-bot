function format(commits) {
  let message = ''

  commits.forEach(c => {
    message += `* Commit: ${c.sha}\n`
    message += c.errors.map(e => `  - âœ– ${e.message}\n`).join('')
    message += c.warnings.map(w => `  - âš  ${w.message}\n`).join('')
  })

  return template.replace('<PLACEHOLDER>', message)
}

